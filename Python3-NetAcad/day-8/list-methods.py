numbers = [111, 7, 2, 1]
print(len(numbers))
print(numbers)

### Appending value. Appends at the end.

numbers.append(4)

print(len(numbers))
print(numbers)

### Inserting value. Inserts at the 0 index of the list. Other elements are shifted one step right.

numbers.insert(0, 222)
print(len(numbers))
print(numbers)

